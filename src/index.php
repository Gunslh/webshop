<script type="text/javascript">
$(function () {
    var obj = null;
    var shift = false;
    var len = 0;

    var shiftFactor = 5;

    function shiftLeft() {
        shift = true;
        var left = obj.css('left');
        var newVal = parseInt(left) - shiftFactor;
        obj.css('left', newVal);
        len -= shiftFactor;

        if (len > 0)
            setTimeout(shiftLeft, 5);
        else
            shift = false;
    }

    function shiftRight() {

        shift = true;
        var left = obj.css('left');
        var newVal = parseInt(left) + shiftFactor;
        obj.css('left', newVal);
        len -= shiftFactor;
        if (len > 0)
            setTimeout(shiftRight, 5);
        else
            shift = false;
    }

    //热门
    $('.index-hotsale .left-arrow').click(function () {
        if (shift == true)
            return;
        obj = $('.sales-container ul');
        var left = parseInt(obj.css('left'));

        if (left <= -1800)
            obj.css('left', 0);

        len = 915;

        shiftLeft();
    });
    $('.index-hotsale .right-arrow').click(function () {

        if (shift == true)
            return;
        obj = $('.sales-container ul');
        var left = obj.css('left');
        len = 915;


        if (parseInt(left) >= 0)
            obj.css('left', -1830);

        shiftRight();
    });
    //操作广告
    var advid = 1;
    var advact = true;
    $('.index-adv .shift ul li').each(function () {
        var id = $(this).attr("showid");
        if (id == 1) {
            $(this).fadeToggle();
        }
    });
    function AdvShow(aid) {
        $('.index-adv .shift ul li').each(function () {
            var id = $(this).attr("showid");
            if (id == aid)
                $(this).fadeIn();
            else
                $(this).fadeOut();
        });
        $('.slide-control span').each(function () {
            var id = $(this).attr("showid");
            if (id == aid)
                $(this).addClass('curr');
            else
                $(this).removeClass('curr');
        });
    };
    $('.index-adv .shift ul li,').hover(function () {
        advact = false;
    });
    $('.slide-control span').hover(function () {
        var id = $(this).attr("showid");
        advact = false;
        AdvShow(id);
    })
    AdvShow(advid);
    function AdvShift() {
        if (advact == false) {
            //alert(0);
            return;
        }
        advid++;
        //alert(advid);
        if (advid > 3)
            advid = 1;
        AdvShow(advid);

    };
    setInterval(AdvShift, 2000);
    //vip 调整
    $('.index-vip').click(function () {
        window.location.href = 'customized.php';
    });
});
